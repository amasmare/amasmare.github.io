function setYear() {
  const y = document.getElementById('year');
  if (y) y.textContent = new Date().getFullYear();
}

function applyLang(lang) {
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const t = I18N[el.getAttribute('data-i18n')];
    if (t && t[lang] != null) el.innerHTML = t[lang];
  });
  document.title = titles[lang];
  // toggle button shows the OTHER language
  const label = document.getElementById('langLabel');
  if (label) label.textContent = lang === 'ar' ? 'English' : 'العربية';
  setYear();
  try { localStorage.setItem('lang', lang); } catch (e) {}
}

// Default to Arabic unless the visitor previously chose a language
let savedLang = 'ar';
try { savedLang = localStorage.getItem('lang') || 'ar'; } catch (e) {}
applyLang(savedLang);

document.getElementById('langToggle').addEventListener('click', () => {
  const next = document.documentElement.lang === 'ar' ? 'en' : 'ar';
  applyLang(next);
});

